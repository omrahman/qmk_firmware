bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case EXIT:
      if (record->event.pressed) {
         layer_and(((layer_state_t)1 << CBASE) | ((layer_state_t)1 << QBASE));
         return false;
      }
      break;
  }
  return true;
}
